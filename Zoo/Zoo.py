while True:
    user_input = input("Please enter the number of the habitat you would like to view or 'exit' to quit: ")

    if user_input == 'exit':
        break

    try:
        habitat_number = int(user_input)

        if 1 <= habitat_number <= len(animals):
            animal = animals[habitat_number - 1]
            print(f"You selected: {animal}")

            animal_name = animal_names.get(animal, "Unnamed")
            if isinstance(animal_name, int):
                print(f"Number: {animal_name}")
            else:
                print(f"Name: {animal_name}")
    except ValueError:
        print("Invalid input. Please enter a valid habitat number.")

